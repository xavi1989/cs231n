int main(int, char**)
{
    VideoCapture cap("test-videos/mona-lisa.avi"); // open the default camera
    if(!cap.isOpened())  // check if we succeeded
        return -1;

    Mat frame1;
    Mat frame2;

    PM tmp;
    // init
    cap.read(frame1);
    frame1.convertTo(frame2, CV_8UC4);
    tmp.initialize(frame2);

    // GLFW window
    display_init();
    drawBackground_init();
    drawmodel_init();

    // Build and compile our shader program
    Shader backgroundShader("texture.vs", "texture.frag");
    Shader modelShader("model.vs", "model.frag");

    while(cap.read(frame1) && !glfwWindowShouldClose(window))
    {
        frame1.convertTo(frame2, CV_8UC4);
        tmp.process_frame(frame2);

        // Check if any events have been activiated (key pressed, mouse moved etc.) and call corresponding response functions
        glfwPollEvents();

        // Render
        // Clear the colorbuffer
        glClearColor(0.1f, 0.1f, 0.1f, 1.0f);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

        // draw background
        GLuint backgroundTexture = loadTexture(frame2);
        // Bind Textures using texture units
        glActiveTexture(GL_TEXTURE0);
        glBindTexture(GL_TEXTURE_2D, backgroundTexture);
        glUniform1i(glGetUniformLocation(backgroundShader.Program, "background"), 0);
        
        // Activate shader
        backgroundShader.Use();       
        
        // Draw container
        glBindVertexArray(VAO);
        glDrawElements(GL_TRIANGLES, 6, GL_UNSIGNED_INT, 0);
        glBindVertexArray(0);

        // Draw cube for now
        Homography H = tmp.getCurrentHomography();
        PointArray currentBound = tmp.getCurrentBound();

        modelShader.Use();
        // Create camera transformations
        glm::mat4 view;
        view = camera.GetViewMatrix();
        glm::mat4 projection = glm::perspective(camera.Zoom, (GLfloat)WIDTH / (GLfloat)HEIGHT, 0.1f, 100.0f);
        // Get the uniform locations
        GLint modelLoc = glGetUniformLocation(modelShader.Program, "model");
        GLint viewLoc  = glGetUniformLocation(modelShader.Program,  "view");
        GLint projLoc  = glGetUniformLocation(modelShader.Program,  "projection");
        // Pass the matrices to the shader
        glUniformMatrix4fv(viewLoc, 1, GL_FALSE, glm::value_ptr(view));
        glUniformMatrix4fv(projLoc, 1, GL_FALSE, glm::value_ptr(projection));
        glm::mat4 model;
        glUniformMatrix4fv(modelLoc, 1, GL_FALSE, glm::value_ptr(model));

        // decompose projection matrix
        mat projMatrix; // 3x4
        mat cameraMatrix; // 3x3
        mat rotMatrix; // 3x3
        mat transVect; // 3x3

        cv::decomposeProjectionMatrix(projMatrix, cameraMatrix, rotMatrix, transVect);



        // Draw the container (using container's vertex attributes)
        glBindVertexArray(model_VAO);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);

        // Swap the screen buffers
        glfwSwapBuffers(window);

        if(waitKey(30) >= 0) break;
    }
    // the camera will be deinitialized automatically in VideoCapture destructor

    // Properly de-allocate all resources once they've outlived their purpose

    glDeleteVertexArrays(1, &VAO);
    glDeleteBuffers(1, &VBO);

    glDeleteVertexArrays(1, &model_VAO);
    glDeleteBuffers(1, &model_VBO);
    // Terminate GLFW, clearing any resources allocated by GLFW.
    glfwTerminate();

    return 0;
}

// Is called whenever a key is pressed/released via GLFW
void key_callback(GLFWwindow* window, int key, int scancode, int action, int mode)
{
    if (key == GLFW_KEY_ESCAPE && action == GLFW_PRESS)
        glfwSetWindowShouldClose(window, GL_TRUE);
    if (key >= 0 && key < 1024)
    {
        if (action == GLFW_PRESS)
            keys[key] = true;
        else if (action == GLFW_RELEASE)
            keys[key] = false;
    }
}
